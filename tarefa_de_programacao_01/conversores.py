def celsius_para_fahrenheit(celsius):
    return celsius * 1.8 + 32

def fahrenheit_para_celsius(fahrenheit):
    return (fahrenheit - 32) / 1.8

def metro_para_pes(metro):
    return metro * 3.281

def pes_para_metros(pes):
    return pes / 3.281

def quilometros_para_milhas(quilometro):
    return quilometro / 1.609

def milhas_para_quilometros(milha):
    return milha * 1.609

def dias_para_horas(dia):
    return dia * 24

